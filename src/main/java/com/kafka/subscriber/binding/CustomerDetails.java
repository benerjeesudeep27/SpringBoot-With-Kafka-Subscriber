package com.kafka.subscriber.binding;

public class CustomerDetails {
	private int id;
	private String name;
	private String email;
	private long phone;
	private String city;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public long getPhone() {
		return phone;
	}
	public void setPhone(long phone) {
		this.phone = phone;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	
	@Override
	public String toString() {
		return "CustomerDetails [id=" + id + ", name=" + name + ", email=" + email + ", phone=" + phone + ", city="
				+ city + "]";
	}
}